#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*res;
	int		i;
	int		length;

	if (s == NULL)
		return (NULL);
	length = ft_strlen(s);
	res = malloc(sizeof(char) * length + 1);
	if (res == NULL)
		return (res);
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
